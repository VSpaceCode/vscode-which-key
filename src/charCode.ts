export const enum CharCode {
    /**
     * The `\t` character.
     */
    Tab = 9,
    /**
     * The ` ` character.
     */
    Space = 32,
    /**
     * The `!` character.
     */
    Exclamation = 33,
    /**
	 * The `/` character.
	 */
    Slash = 47,
	/**
	 * The `:` character.
	 */
    Colon = 58,

    A = 65,
    Z = 90,

    /**
     * The `\` character.
     */
    Backslash = 92,

    a = 97,
    z = 122,

    /**
     * The `~` character.
     */
    Tide = 126
}
